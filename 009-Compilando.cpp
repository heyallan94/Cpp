/*
Compilar código é o processo de traduzir o código-fonte escrito em uma linguagem de programação 
para um código executável que pode ser entendido e executado pelo computador. 
O compilador é responsável por realizar essa tradução, convertendo o código-fonte em uma 
forma de código que a máquina pode entender e executar diretamente.

Linguagens de programação podem ser classificadas em duas categorias principais: 
linguagens compiladas e linguagens interpretadas.

Linguagens compiladas: Nesse tipo de linguagem, o código-fonte é traduzido 
integralmente para código de máquina antes de ser executado. 

Algumas linguagens compiladas comuns incluem:
C e C++
Java (que é compilada para bytecode e executada em uma máquina virtual Java - JVM)
Rust
Go
Swift

Ao compilar um programa nessas linguagens, é gerado um arquivo executável 
específico para o sistema operacional e a arquitetura em que o código está sendo compilado.

Linguagens interpretadas: 
Nessas linguagens, o código-fonte é executado linha a linha ou instrução por 
instrução, em tempo real, por um interpretador. Algumas linguagens interpretadas comuns incluem:
Python
JavaScript
Ruby
PHP

Essas linguagens geralmente não produzem um arquivo executável binário, 
mas são executadas por meio de um interpretador específico da linguagem.

No caso do Python, embora seja considerada uma linguagem interpretada, 
há um processo de compilação intermediária. O código-fonte Python é compilado 
para bytecode em arquivos .pyc (arquivos de código compilado) para melhorar o 
desempenho da execução subsequente. O interpretador Python então executa esse bytecode.

Já o JavaScript é uma linguagem interpretada, o que significa 
que o código-fonte JavaScript é interpretado e executado diretamente em tempo real 
pelos navegadores ou pelos ambientes de execução JavaScript, como o Node.js.

No geral, a compilação de código é o processo de transformar o código-fonte 
em um formato executável, enquanto a interpretação ocorre linha a linha ou 
instrução por instrução, sem um passo intermediário de compilação para um código de máquina específico.

É geralmente correto dizer que linguagens compiladas podem ter um desempenho 
maior em comparação com linguagens interpretadas. Isso ocorre porque, ao serem compiladas, 
o código-fonte é traduzido diretamente para código de máquina, que é executado de forma eficiente 
pela CPU. Isso elimina a necessidade de interpretar o código linha a linha durante a execução, 
o que pode adicionar um overhead de desempenho
*/