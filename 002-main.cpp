/*
Assim como outras linguagens, C++ tem funções
============== IMPORTANTE ===========
Em C++ você precisa declarar uma função MAIN

Um código em C++ precisa ter uma função main() para que seja executado corretamente. 
A função main() é o ponto de entrada do programa em C++. É a partir dessa função que o programa começa 
sua execução.
A estrutura básica de um programa em C++ é composta pela função main(), 
que pode conter chamadas a outras funções e instruções para realizar determinadas tarefas. 
É dentro da função main() que você escreve o código que deseja executar.

Se você escrever um código em C++ sem a função main(), o compilador retornará um erro, 
indicando que não foi encontrada a função de entrada do programa. 
Isso ocorre porque o sistema operacional ou o ambiente de execução espera que um programa 
C++ tenha uma função main() para iniciar a execução.

Aqui está um exemplo básico de um programa C++ com a função main()
*/

int main(){ //O tipo de dado que volta é um 'int' de integer/inteiro, ou seja, o dado retornado é um NÚMERO INTEIRO
    return 3;//Note que 0 é um número inteiro
}